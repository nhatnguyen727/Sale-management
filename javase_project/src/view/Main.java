    private void resetColor(JPanel[] pane, JPanel[] indicators) {
        for (JPanel pane1 : pane) {
            pane1.setBackground(new Color(13, 0, 13));
        }
        for (JPanel indicator : indicators) {
            indicator.setOpaque(false);
        }
    }

