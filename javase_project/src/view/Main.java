    public class KGradientPanel extends JPanel {

        Color gradientStart;
        Color gradientEnd;
        int gradientFocus;
        int roundCorner;

        public KGradientPanel(Color gradientStart, Color gradientEnd, int gradientFocus, int roundCorner) {
            this.gradientStart = gradientStart;
            this.gradientEnd = gradientEnd;
            this.gradientFocus = gradientFocus;
            this.roundCorner = roundCorner;
        }

        @Override
        protected void paintComponent(Graphics g) {
            super.paintComponent(g);
            Graphics2D g2d = (Graphics2D) g;
            g2d.setRenderingHint(RenderingHints.KEY_RENDERING, RenderingHints.VALUE_RENDER_QUALITY);
            g2d.setRenderingHint(RenderingHints.KEY_ANTIALIASING, RenderingHints.VALUE_ANTIALIAS_ON);

            int w = getWidth();
            int h = getHeight();
            Dimension arcs = new Dimension(roundCorner, roundCorner);

            GradientPaint gp = new GradientPaint(0, 0, gradientStart, gradientFocus, h, gradientEnd);

            g2d.setPaint(gp);
            g2d.fillRoundRect(0, 0, w - 1, h - 1, arcs.width, arcs.height);
            g2d.drawRoundRect(0, 0, w - 1, h - 1, arcs.width, arcs.height);
        }
    }

