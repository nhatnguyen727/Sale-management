    private void clock() {
        Thread thread = new Thread() {
            @Override
            public void run() {
                try {
                    while (clockThread == 1) {
                        if (cmbLanguage.getSelectedItem().toString().equals("English")) {
                            String currentDate = new java.util.Date().toString();
                            jLabel_date.setText(currentDate.substring(0, 11) + " " + currentDate.substring(currentDate.length() - 4));
                        } else {
                            int dweek = LocalDate.now().getDayOfWeek().getValue() + 1;
                            int dmonth = LocalDate.now().getDayOfMonth();
                            int month = LocalDate.now().getMonthValue();
                            int year = LocalDate.now().getYear();
                            if (dweek == 8) {
                                jLabel_date.setText("Chủ nhật ngày " + dmonth + " tháng " + month + " năm " + year);
                            } else {
                                jLabel_date.setText("Thứ " + dweek + " ngày " + dmonth + " tháng " + month + " năm " + year);
                            }
                        }

                        jLabel_time.setText(LocalTime.now().toString().substring(0, 8));

                        sleep(1000);
                    }
                } catch (InterruptedException e) {
                    java.util.logging.Logger.getLogger(Main.class.getName()).log(java.util.logging.Level.SEVERE, null, e);
                }
            }
        };
        thread.start();
    }

