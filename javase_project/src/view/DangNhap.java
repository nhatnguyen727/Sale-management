    public void login() {
        Account account = controller.Accountcontroller.GetAccountByUserName(txt_username.getText());
        if (account == null) {
            new view.Message("Sai tài khoản. Hãy nhập lại", 2, 1).setVisible(true);
        } else {
            String md5pass = controller.MD5Encrypt.encryptPass(new String(txt_password.getPassword()));
            if (account.getPassword().equals(md5pass)) {
                loader.setVisible(true);
                login.setVisible(false);
                accountlogin = controller.Accountcontroller.GetAccountByUserName(txt_username.getText());
                new java.util.Timer().schedule(new TimerTask() {
                    @Override
                    public void run() {
                Main h = new Main();
                h.setVisible(true);
                dispose();
                    }
                }, 1000 * 5);
            } else {
                new view.Message("Sai mật khẩu. Hãy nhập lại", 2, 1).setVisible(true);
            }
        }
    }
    
