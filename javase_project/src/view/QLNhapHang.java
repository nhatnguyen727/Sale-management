    private void txt_search_receiptnotedetailKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_search_receiptnotedetailKeyReleased
        Vector data = new Vector();
        for (int i = 0; i < modelTableReceiptNoteDetails.getRowCount(); i++) {
            for (int j = 0; j < modelTableReceiptNoteDetails.getColumnCount(); j++) {
                if (modelTableReceiptNoteDetails.getValueAt(i, j).toString().toUpperCase().contains(txt_search_receiptnotedetail.getText().toUpperCase())) {
                    Vector dongdulieu = new Vector();
                    for (int k = 0; k < modelTableReceiptNoteDetails.getColumnCount(); k++) {
                        dongdulieu.add(modelTableReceiptNoteDetails.getValueAt(i, k));
                    }
                    data.add(dongdulieu);
                    break;
                }
            }
        }
        DefaultTableModel model = new DefaultTableModel(data, titlesTableReceiptNoteDetails) {
            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
        tbl_receiptnotedetail.setModel(model);
    }//GEN-LAST:event_txt_search_receiptnotedetailKeyReleased

