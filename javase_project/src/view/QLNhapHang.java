    private void jTextField4KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField4KeyReleased
        Vector data = new Vector();
        for (int i = 0; i < modelTableProduct.getRowCount(); i++) {
            for (int j = 0; j < modelTableProduct.getColumnCount(); j++) {
                if (modelTableProduct.getValueAt(i, j).toString().toUpperCase().contains(jTextField4.getText().toUpperCase())) {
                    Vector dongdulieu = new Vector();
                    for (int k = 0; k < modelTableProduct.getColumnCount(); k++) {
                        dongdulieu.add(modelTableProduct.getValueAt(i, k));
                    }
                    data.add(dongdulieu);
                    break;
                }
            }
        }
        DefaultTableModel model = new DefaultTableModel(data, titlesTableProduct) {
            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
        tbl_product.setModel(model);
    }//GEN-LAST:event_jTextField4KeyReleased

