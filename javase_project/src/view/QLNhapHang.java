    private void jButton4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton4MouseClicked
        Vector titlesProv = new Vector();
        titlesProv.add("ID nhà cung cấp");
        titlesProv.add("Tên nhà cung cấp");
        titlesProv.add("Địa chỉ");
        titlesProv.add("Số điện thoại");
        Vector bangdulieu = new Vector();
        List<Provider> list = controller.ProviderController.GetListProvider();
        for (Provider prov : list) {
            Vector dongdulieu = new Vector();
            dongdulieu.add(prov.getProviderID());
            dongdulieu.add(prov.getProviderName());
            dongdulieu.add(prov.getProviderAddress());
            dongdulieu.add(prov.getTellNo());
            bangdulieu.add(dongdulieu);
        }
        DefaultTableModel defaultTableModel = new DefaultTableModel(bangdulieu, titlesProv) {
            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
        tbl_provider1.setModel(defaultTableModel);
        dlg_provider.pack();
        dlg_provider.setLocationRelativeTo(null);
        dlg_provider.setVisible(true);

        jDialog1.dispose();
    }//GEN-LAST:event_jButton4MouseClicked

