    private void jTextField3KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField3KeyReleased
        Vector data = new Vector();
        for (int i = 0; i < modelTableProvider.getRowCount(); i++) {
            for (int j = 0; j < modelTableProvider.getColumnCount(); j++) {
                if (modelTableProvider.getValueAt(i, j).toString().toUpperCase().contains(jTextField3.getText().toUpperCase())) {
                    Vector dongdulieu = new Vector();
                    for (int k = 0; k < modelTableProvider.getColumnCount(); k++) {
                        dongdulieu.add(modelTableProvider.getValueAt(i, k));
                    }
                    data.add(dongdulieu);
                    break;
                }
            }
        }
        DefaultTableModel model = new DefaultTableModel(data, titlesTableProvider) {
            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
        tbl_provider1.setModel(model);
    }//GEN-LAST:event_jTextField3KeyReleased

