    private void btn_addProvMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_addProvMouseClicked
        if (btn_addProv.getText().trim().equalsIgnoreCase("Thêm")) {
            Provider newprov = new Provider();
            if ((txt_provName.getText().isEmpty()) || txt_provAddress.getText().isEmpty() || txt_provPhone.getText().isEmpty()) {
                new Message("Bạn cần nhập đầy đủ thông tin", 2, 3).setVisible(true);
            } else {
                if (Validation.isPhoneNumber(txt_provPhone.getText().trim()) == false) {
                    new Message("Số điện thoại không đúng", 2, 3).setVisible(true);
                } else {
                    if (controller.ProviderController.checkExistTellNoProvider(txt_provPhone.getText().trim())) {
                        new Message("Số điện thoại đã được sử dụng", 2, 3).setVisible(true);
                    } else {
                        newprov.setProviderName(txt_provName.getText().trim());
                        newprov.setTellNo(txt_provPhone.getText().trim());
                        newprov.setProviderAddress(txt_provAddress.getText().trim());

                        controller.ProviderController.AddProvider(newprov);
                        new Message("Thêm nhà cung cấp thành công", 1, 3).setVisible(true);
                        loadTableProvider();
                    }
                }
            }
        }
        if (btn_addProv.getText().trim().equalsIgnoreCase("Cập nhật")) {
            Provider newprov = new Provider();
            if ((txt_provName.getText().isEmpty()) || txt_provAddress.getText().isEmpty() || txt_provPhone.getText().isEmpty()) {
                new Message("Bạn cần nhập đầy đủ thông tin", 2, 3).setVisible(true);
            } else {
                if (Validation.isPhoneNumber(txt_provPhone.getText().trim()) == false) {
                    new Message("Số điện thoại không đúng", 2, 3).setVisible(true);
                } else {
                    if (controller.ProviderController.checkTellNoOnUpdate(Integer.parseInt(lb_provID.getText().substring(18)), txt_provPhone.getText().trim()) == false) {
                        new Message("Số điện thoại đã được sử dụng", 2, 3).setVisible(true);
                    } else {
                        newprov.setProviderID(Integer.parseInt(lb_provID.getText().substring(18)));
                        newprov.setProviderName(txt_provName.getText().trim());
                        newprov.setTellNo(txt_provPhone.getText().trim());
                        newprov.setProviderAddress(txt_provAddress.getText().trim());
                        controller.ProviderController.EditProvider(newprov);
                        new Message("Cập nhật nhà cung cấp thành công", 1, 3).setVisible(true);
                        loadTableProvider();
                    }
                }
            }
        }
    }//GEN-LAST:event_btn_addProvMouseClicked

